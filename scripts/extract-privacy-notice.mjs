import { PDFParse } from 'pdf-parse';
import fs from 'fs';
import path from 'path';

const PDF_PATH = path.resolve(import.meta.dirname, '..', 'Privacy Notice.pdf');
const OUTPUT_PATH = path.resolve(import.meta.dirname, '..', 'src', 'lib', 'privacy-notice.ts');
const SECTION_REGEX = /^(\d+)\.\s+(.+)$/;

async function extractPdfText(filePath) {
	const buf = fs.readFileSync(filePath);
	const parser = new PDFParse({ data: buf });
	const result = await parser.getText();
	return result.text;
}

function cleanText(text) {
	return text
		.split('\n')
		.filter((line) => !/^--\s+\d+\s+of\s+\d+\s+--$/.test(line.trim()))
		.join('\n')
		.trim();
}

function isSubheading(line) {
	const trimmed = line.trim();
	if (trimmed.startsWith('\u25CF')) return false;
	if (trimmed.length > 60) return false;
	if (/[.:;!?]$/.test(trimmed)) return false;
	if (/\d/.test(trimmed)) return false;
	if (/@/.test(trimmed)) return false;
	if (/^www\./i.test(trimmed)) return false;
	// Company suffixes are not sub-headings
	if (/\b(Ltd|Limited|Inc|Corporation|Corp|LLC|PLC)\b\.?$/.test(trimmed)) return false;
	const wordCount = trimmed.split(/\s+/).length;
	return wordCount >= 1 && wordCount <= 6;
}

function parseBlocks(text) {
	const lines = text.split('\n').filter((l) => l.trim().length > 0);
	const blocks = [];
	let i = 0;

	while (i < lines.length) {
		const line = lines[i].trim();

		if (line.startsWith('\u25CF')) {
			const items = [];
			while (i < lines.length && lines[i].trim().startsWith('\u25CF')) {
				items.push(lines[i].trim().replace(/^\u25CF\s*/, ''));
				i++;
			}
			// Check if the next line is a word-wrapped continuation of the last item
			if (i < lines.length) {
				const nextLine = lines[i].trim();
				if (
					!nextLine.startsWith('\u25CF') &&
					/^[a-z]/.test(nextLine) &&
					!isSubheading(nextLine)
				) {
					items[items.length - 1] += ' ' + nextLine;
					i++;
				}
			}
			blocks.push({ kind: 'list', items });
		} else if (isSubheading(line)) {
			blocks.push({ kind: 'subheading', text: line });
			i++;
		} else {
			const paraLines = [line];
			i++;
			while (i < lines.length) {
				const nextLine = lines[i].trim();
				if (nextLine.startsWith('\u25CF') || isSubheading(nextLine)) {
					break;
				}
				paraLines.push(nextLine);
				i++;
			}
			blocks.push({ kind: 'paragraph', text: paraLines.join('\n') });
		}
	}

	return blocks;
}

function parseSections(text) {
	const lines = text.split('\n');
	const sections = [];
	let currentSection = null;

	for (const line of lines) {
		const trimmed = line.trim();
		const match = trimmed.match(SECTION_REGEX);

		if (match) {
			if (currentSection) {
				sections.push({
					id: currentSection.id,
					title: currentSection.title,
					contentLines: currentSection.contentLines,
				});
			}
			currentSection = {
				id: Number.parseInt(match[1], 10),
				title: match[2],
				contentLines: [],
			};
		} else if (currentSection && trimmed.length > 0) {
			currentSection.contentLines.push(trimmed);
		}
	}

	if (currentSection) {
		sections.push({
			id: currentSection.id,
			title: currentSection.title,
			contentLines: currentSection.contentLines,
		});
	}

	return sections;
}

function escapeForTemplateLiteral(text) {
	return text.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$');
}

function generateBlockValue(block) {
	// Caller prepends 3 tabs (\t\t\t) for block-level opening.
	// This function uses relative indentation:
	//   \t{              -> block open  (4 tabs total)
	//   \t\tkind:         -> property   (5 tabs total)
	//   \t\ttext:         -> property   (5 tabs total)
	//   \t\titems: [      -> array open (5 tabs total)
	//   \t\t\t`item`,    -> list item  (6 tabs total)
	//   \t\t],            -> array close(5 tabs total)
	//   \t}              -> block close(4 tabs total)
	switch (block.kind) {
		case 'paragraph':
		case 'subheading':
			return `\t{\n\t\tkind: '${block.kind}',\n\t\ttext: \`${escapeForTemplateLiteral(block.text)}\`,\n\t}`;
		case 'list': {
			const items = block.items.map(
				(item) => `\t\t\t\`${escapeForTemplateLiteral(item)}\``,
			);
			return `\t{\n\t\tkind: 'list',\n\t\titems: [\n${items.join(',\n')},\n\t\t],\n\t}`;
		}
		default:
			return '';
	}
}

function generateTypeScriptFile(title, lastUpdated, sections) {
	const sectionEntries = sections
		.map((section) => {
			const blocks = parseBlocks(section.contentLines.join('\n'));
			const blockEntries = blocks
				.map((block) => `\t\t\t${generateBlockValue(block)}`)
				.join(',\n');

			return `\t\t{\n\t\t\tid: ${section.id},\n\t\t\ttitle: \`${escapeForTemplateLiteral(section.title)}\`,\n\t\t\tblocks: [\n${blockEntries},\n\t\t\t],\n\t\t}`;
		})
		.join(',\n\n');

	return `// This file is auto-generated by scripts/extract-privacy-notice.mjs.
// Do not edit manually — edit Privacy Notice.pdf and re-run the script.

export interface ContentBlock {
	kind: 'paragraph' | 'subheading' | 'list';
	text?: string;
	items?: string[];
}

export interface PrivacySection {
	id: number;
	title: string;
	blocks: ContentBlock[];
}

export interface PrivacyNotice {
	title: string;
	lastUpdated: string;
	sections: PrivacySection[];
}

export const privacyNotice: PrivacyNotice = {
	title: \`${escapeForTemplateLiteral(title)}\`,
	lastUpdated: \`${escapeForTemplateLiteral(lastUpdated)}\`,
	sections: [
${sectionEntries},
	],
};
`;
}

async function main() {
	const rawText = await extractPdfText(PDF_PATH);
	const cleaned = cleanText(rawText);
	const lines = cleaned.split('\n');

	const title = lines[0];
	const lastUpdatedLine = lines[1] || '';
	const lastUpdated = lastUpdatedLine.replace(/^Last Updated:\s*/i, '');
	const bodyText = lines.slice(2).join('\n');
	const sections = parseSections(bodyText);

	const output = generateTypeScriptFile(title, lastUpdated, sections);
	fs.writeFileSync(OUTPUT_PATH, output, 'utf-8');

	console.log(`✓ Extracted ${sections.length} sections from "${title}"`);
	console.log(`  → ${OUTPUT_PATH}`);
}

main().catch((err) => {
	console.error('Failed to extract privacy notice:', err);
	process.exit(1);
});
